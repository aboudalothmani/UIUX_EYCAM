class I18n(QObject):
    languageChanged = Signal()

    def __init__(self):
        super().__init__()
        self._language = "en"

    @Property(str, notify=languageChanged)
    def language(self):
        return self._language

    @language.setter
    def language(self, value: str):
        if value in STRINGS and value != self._language:
            self._language = value
            self.languageChanged.emit()

    @Slot(str, result=str)
    def t(self, key: str) -> str:
        table = STRINGS.get(self._language) or STRINGS["en"]
        return table.get(key, STRINGS["en"].get(key, key))

    @Slot()
    def toggleLanguage(self):
        self.language = "ar" if self._language == "en" else "en"

    @Slot(str)
    def setLanguage(self, code: str):
        if code in STRINGS:
            self.language = code


